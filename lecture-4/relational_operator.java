public class relational_operator {
    public static void main(String[] args){
  //lecture of relational operators     
System.out.println(2<1);//less than
System.out.println(2>1);//greater than
System.out.println(2 <= 2);//less or equal
System.out.println(2>=1);//less or greater
System.out.println(2==3);//equals
System.out.println(2!=1);//not equal
    }
}